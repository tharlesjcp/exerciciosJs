//Escrever um algoritmo que receba um número e mostre uma mensagem caso este número seja
//maior que 80, menor que 25 ou igual a 40
let compare
compare=prompt("Digite um número!! ")
if(compare >= 80) {
    prompt("É maior que oitenta meu parça! ")
} else if(compare < 25) {
    prompt("É menor que vinte e cinco capitão")
}else if(compare == 40) {
    prompt("É igual a quarenta!!")
}